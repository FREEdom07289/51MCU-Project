/*
 * LED闪烁实验 - 普中A2开发板
 * 功能：LED0以固定频率闪烁（P2.0引脚）
 * 芯片：STC89C52RC
 * 晶振：11.0592MHz
 * 知识点：软件延时、while(1)循环、I/O翻转控制
 */

#include <reg52.h>  // 51单片机标准头文件

// 定义LED引脚，普中A2开发板上LED0连接到P2.0
sbit LED = P2^0;    // sbit 是Keil C51扩展关键字，用于位寻址

/*
 * 软件延时函数
 * 功能：通过空循环消耗CPU时间，实现约500ms延时
 * 原理：嵌套两层循环，执行约 200×200 = 40000 次空操作
 *       在11.0592MHz晶振下，一次循环约几微秒
 *       实际延时 ≈ 40000 × 几微秒 ≈ 数百毫秒
 * 注意：此方法不精确，受编译器优化和晶振频率影响
 *       精确延时应使用定时器（后续课程会讲）
 */
void Delay(void)
{
    unsigned int i, j;
    for(i = 0; i < 200; i++)
    {
        for(j = 0; j < 200; j++);  // 内层空循环，每次循环约几微秒
    }
}

void main(void)
{
    // 初始状态：熄灭LED（高电平）
    // 普中A2开发板LED为共阳极接法：低电平点亮，高电平熄灭
    LED = 1;

    while(1)        // 主循环，单片机程序一般永不退出
    {
        LED = 0;    // 输出低电平 → 点亮LED0
        Delay();    // 保持点亮状态约500ms

        LED = 1;    // 输出高电平 → 熄灭LED0
        Delay();    // 保持熄灭状态约500ms
    }
}
